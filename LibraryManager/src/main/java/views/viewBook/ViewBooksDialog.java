    private void booksListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_booksListMouseClicked
        int selectedIndex = booksList.getSelectedIndex();
        Book book = (Book) booksModel.get(selectedIndex);
        idField.setValue(null);
        showBookData(book);
        selectedBook = book;
        lendingsHistoryButton.setEnabled(true);
    }//GEN-LAST:event_booksListMouseClicked

    private void searchButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchButtonMouseClicked
        Book book = controller.getById(parseInt(idField.getText()));
        if(book != null) {
            booksList.clearSelection();
            showBookData(book);
            selectedBook = book;
            lendingsHistoryButton.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Book id not found");
        }
    }//GEN-LAST:event_searchButtonMouseClicked

    private void lendingsHistoryButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lendingsHistoryButtonMouseClicked
        if(selectedBook == null) { // Modal shouldn't open with no user selected
            return;
        }
        BookLendingsHistoryDialog lendingsHistoryDialog = new BookLendingsHistoryDialog(parent, rootPaneCheckingEnabled);
        lendingsHistoryDialog.setBook(selectedBook);
        lendingsHistoryDialog.setVisible(true);
    }//GEN-LAST:event_lendingsHistoryButtonMouseClicked

    private void showBookData(Book book) {
        // String fields:
        titleLabel.setText(book.getTitle());
        authorLabel.setText(book.getAuthorName());
        
        // Integer fields:
        editionLabel.setText(Integer.toString(book.getEdition()));
        idLabel.setText(Integer.toString(book.getId()));
        copiesOwnedLabel.setText(Integer.toString(book.getCopiesOwned()));
        
        // Date field:
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        releaseDateLabel.setText(dateFormat.format(book.getReleaseDate()));
    }
    
