    public void loadBooks() {
        List<Book> books = controller.getAll();
        
        booksModel.clear();
        
        for(int i = 0; i < books.size(); i++) {
            Book book = books.get(i);
            booksModel.addElement(book);
        }
        
        booksList.setModel(booksModel);
    }

