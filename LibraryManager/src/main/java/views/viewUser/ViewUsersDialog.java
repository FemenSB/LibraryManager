    public void loadUsers() {
        List<User> users = controller.getAll();
        
        usersModel.clear();
        
        for(int i = 0; i < users.size(); i++) {
            User user = users.get(i);
            usersModel.addElement(user);
        }
        
        usersList.setModel(usersModel);
    }

