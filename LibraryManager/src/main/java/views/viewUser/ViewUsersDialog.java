    private void usersListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_usersListMouseClicked
        int selectedIndex = usersList.getSelectedIndex();
        User user = (User) usersModel.get(selectedIndex);
        idField.setValue(null);
        showUserData(user);
        selectedUser = user;
        payFineButton.setEnabled(true);
        lendingsHistoryButton.setEnabled(true);
    }//GEN-LAST:event_usersListMouseClicked

    private void searchButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchButtonMouseClicked
        User user = controller.getById(parseInt(idField.getText()));
        if(user != null) {
            usersList.clearSelection();
            showUserData(user);
            selectedUser = user;
            payFineButton.setEnabled(true);
            lendingsHistoryButton.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "User id not found");
        }
    }//GEN-LAST:event_searchButtonMouseClicked

