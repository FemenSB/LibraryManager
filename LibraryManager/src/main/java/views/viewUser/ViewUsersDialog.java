    private void payFineButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_payFineButtonMouseClicked
        if(selectedUser == null) { // Modal shouldn't open with no user selected
            return;
        }
        PayFineDialog payFineDialog = new PayFineDialog(parent, rootPaneCheckingEnabled);
        payFineDialog.setUser(selectedUser);
        payFineDialog.setVisible(true);
    }//GEN-LAST:event_payFineButtonMouseClicked

    private void lendingsHistoryButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lendingsHistoryButtonMouseClicked
        if(selectedUser == null) { // Modal shouldn't open with no user selected
            return;
        }
        LendingsHistoryDialog lendingsHistoryDialog = new LendingsHistoryDialog(parent, rootPaneCheckingEnabled);
        lendingsHistoryDialog.setUser(selectedUser);
        lendingsHistoryDialog.setVisible(true);
    }//GEN-LAST:event_lendingsHistoryButtonMouseClicked

    private void showUserData(User user) {
        nameLabel.setText(user.getName());
        phoneNumberLabel.setText(user.getPhoneNumber());
        fineLabel.setText(String.valueOf(user.getFine()));
        idLabel.setText(Integer.toString(user.getId()));
    }
    
